// Misc Instructions
`define NOP 4'b0001

// ALU Ops (Arithmetic and Logic)
`define ADD 4'b0010
`define SUB 4'b0011
`define SHL 4'b0100
`define SHR 4'b0101

// Branches
`define BNE 4'b0110

`define JMP 4'b0111
`define RET 4'b1000

// Load/Store
`define LDI 4'b1001

`define OTR 4'b1010
`define HLT 4'b1011
